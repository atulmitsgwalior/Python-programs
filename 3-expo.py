# Prog 3- Exponentiation (power of number)
# Using the built-in pow() function
base = 2
exponent = 3
result = pow(base, exponent)
print(f" Result-1 {base} raised to the power of {exponent} is: {result}")



# Using the ** operator
base = 2
exponent = 3
result = base ** exponent
print(f"Result-2 {base} raised to the power of {exponent} is: {result}")



# Using math.pow() function
import math
base = 2
exponent = 3
result = math.pow(base, exponent)
print(f"Result-3 {base} raised to the power of {exponent} is: {result}")
